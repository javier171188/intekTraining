test('Small matrixes', () => {
    let A = [];
    let area = greatestArea(A);
    expect(area).toBe(0);

    A = [[0]];
    area = greatestArea(A);
    expect(area).toBe(0);

    A = [[1]];
    area = greatestArea(A);
    expect(area).toBe(1);

    A = [
        [1, 0],
        [0, 1]
    ];
    area = greatestArea(A);
    expect(area).toBe(1);

    A = [
        [1, 1],
        [0, 1]
    ];
    area = greatestArea(A);
    expect(area).toBe(2);
})